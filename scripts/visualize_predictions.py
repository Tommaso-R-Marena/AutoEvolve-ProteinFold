class ProteinVisualizer:
    """Create sophisticated 3D visualizations with confidence coloring."""
    
    def __init__(self, model_path: str):
        self.model = EvolvableProteinFoldingModel.load_checkpoint(model_path)
        self.model.eval()
        self.data_generator = ProteinDataGenerator()
    
    def predict_structure(self, sequence: str) -> Dict:
        """Predict structure for a sequence."""
        # Convert sequence to tensor
        seq_tensor = torch.zeros(1, len(sequence), dtype=torch.long)
        for i, aa in enumerate(sequence):
            if aa in self.data_generator.AMINO_ACID_TO_IDX:
                seq_tensor[0, i] = self.data_generator.AMINO_ACID_TO_IDX[aa]
        
        with torch.no_grad():
            predictions = self.model(seq_tensor)
        
        return {
            'coordinates': predictions['coordinates'][0].numpy(),
            'confidence': predictions['confidence'][0].numpy(),
            'angles': predictions['angles'][0].numpy(),
            'sequence': sequence
        }
    
    def save_pdb(self, predictions: Dict, output_path: str):
        """Save predictions as PDB file with confidence in B-factor column."""
        coords = predictions['coordinates']
        confidence = predictions['confidence']
        sequence = predictions['sequence']
        
        with open(output_path, 'w') as f:
            f.write("HEADER    PREDICTED PROTEIN STRUCTURE\n")
            f.write("REMARK    Generated by AutoEvolve-ProteinFold\n")
            f.write(f"REMARK    Model Generation: {self.model.generation}\n")
            f.write("REMARK    B-factor column contains prediction confidence (0-100)\n")
            
            for i, (coord, conf, aa) in enumerate(zip(coords, confidence, sequence), start=1):
                # PDB format for CA atoms
                x, y, z = coord
                b_factor = conf * 100  # Scale to 0-100
                
                f.write(
                    f"ATOM  {i:5d}  CA  {aa:3s} A{i:4d}    "
                    f"{x:8.3f}{y:8.3f}{z:8.3f}  1.00{b_factor:6.2f}           C\n"
                )
            
            f.write("END\n")
    
    def create_pymol_visualization(self, predictions: Dict, output_path: str,
                                   style: str = 'cartoon', show_confidence: bool = True):
        """Create publication-quality PyMOL visualization."""
        if not PYMOL_AVAILABLE:
            print("PyMOL not available for visualization")
            return
        
        # Save temporary PDB
        with tempfile.NamedTemporaryFile(mode='w', suffix='.pdb', delete=False) as tmp:
            tmp_pdb = tmp.name
            self.save_pdb(predictions, tmp_pdb)
        
        # Initialize PyMOL in quiet mode
        pymol.finish_launching(['pymol', '-cq'])
        
        try:
            # Load structure
            cmd.load(tmp_pdb, 'protein')
            
            # Set visualization style
            cmd.hide('everything', 'protein')
            
            if style == 'cartoon':
                cmd.show('cartoon', 'protein')
                cmd.set('cartoon_smooth_loops', 0)
                cmd.set('cartoon_fancy_helices', 1)
            elif style == 'ribbon':
                cmd.show('ribbon', 'protein')
            elif style == 'sticks':
                cmd.show('sticks', 'protein')
            elif style == 'surface':
                cmd.show('surface', 'protein')
                cmd.set('transparency', 0.3)
            
            if show_confidence:
                # Color by confidence (B-factor)
                # Red (low) -> Yellow (medium) -> Green (high)
                cmd.spectrum('b', 'red_yellow_green', 'protein')
            else:
                # Default coloring by secondary structure
                cmd.color('cyan', 'protein')
            
            # Set up nice rendering
            cmd.bg_color('white')
            cmd.set('ray_shadows', 0)
            cmd.set('antialias', 2)
            cmd.set('ambient', 0.4)
            cmd.set('specular', 0.5)
            
            # Orient and zoom
            cmd.orient('protein')
            cmd.zoom('protein', 2)
            
            # Render high-quality image
            cmd.png(output_path, width=1920, height=1080, dpi=300, ray=1)
            
            print(f"✓ Saved PyMOL visualization: {output_path}")
            
        finally:
            cmd.delete('all')
            Path(tmp_pdb).unlink(missing_ok=True)
    
    def create_multiple_views(self, predictions: Dict, output_dir: str):
        """Create multiple visualization angles and styles."""
        if not PYMOL_AVAILABLE:
            return
        
        output_dir = Path(output_dir)
        output_dir.mkdir(parents=True, exist_ok=True)
        
        # Save PDB
        pdb_path = output_dir / 'structure.pdb'
        self.save_pdb(predictions, str(pdb_path))
        
        pymol.finish_launching(['pymol', '-cq'])
        
        try:
            cmd.load(str(pdb_path), 'protein')
            
            views = [
                ('front', [0, 0, 0]),
                ('side', [90, 0, 0]),
                ('top', [0, 90, 0])
            ]
            
            styles_config = [
                ('cartoon_confidence', 'cartoon', True),
                ('surface_confidence', 'surface', True),
                ('ribbon', 'ribbon', False)
            ]
            
            for style_name, style, show_conf in styles_config:
                cmd.hide('everything', 'protein')
                
                if style == 'cartoon':
                    cmd.show('cartoon', 'protein')
                    cmd.set('cartoon_fancy_helices', 1)
                elif style == 'surface':
                    cmd.show('surface', 'protein')
                    cmd.set('transparency', 0.2)
                elif style == 'ribbon':
                    cmd.show('ribbon', 'protein')
                
                if show_conf:
                    cmd.spectrum('b', 'red_yellow_green', 'protein')
                else:
                    cmd.color('cyan', 'protein')
                
                cmd.bg_color('white')
                cmd.set('ray_shadows', 0)
                cmd.set('antialias', 2)
                
                for view_name, rotation in views:
                    cmd.reset()
                    cmd.orient('protein')
                    cmd.rotate('x', rotation[0])
                    cmd.rotate('y', rotation[1])
                    cmd.rotate('z', rotation[2])
                    cmd.zoom('protein', 2)
                    
                    output_file = output_dir / f'{style_name}_{view_name}.png'
                    cmd.png(str(output_file), width=1920, height=1080, dpi=300, ray=1)
            
            print(f"✓ Created {len(styles_config) * len(views)} visualizations in {output_dir}")
            
        finally:
            cmd.delete('all')
    
